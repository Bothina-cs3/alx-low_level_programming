#include "3-calc.h"
/**
 * op_add - adds two int
 * @a: firest int
 * @b: second int
 * Return: sum a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts tow int
 * @a: firest int
 * @b: second int
 * Return: difference a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies tow int
 * @a: firest int
 * @b: second int
 * Return: product of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides tow int
 * @a: firest int
 * @b: second int
 * Return: result of the division of a by b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo tow int
 * @a: firest int
 * @b: second int
 * Return: remainder of the division of a by b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
